import mongoose from "mongoose";

//creating schema for user po
const postSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      max: 500,
    },
    img: {
      type: String,
    },
    likes: {
      type: Array,
      default: [],
    },
  },
  { timestamps: true }
);
const userschema = mongoose.model("Post", postSchema);
export default userschema;
